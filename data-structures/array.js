/**Big O of Arrays**
 *   Insertion - depends
        - inserting at the end - O(1)
        - inserting at the begining - O(n)
 *   Removal - depends
        - removing at the end - O(1)
        - removing at the begining - O(n)
 *   Access - O(1)
 *   Searching - O(n)
 */

/**Big O of Array Methods**
 *   push() - O(1)
 *   pop() - O(1)
 *   shift() - O(n)
 *   unshift - O(n)
 *   concat - O(n)
 *   slice - O(n)
 *   splice - O(n)
 *   sort - O(n log n)
 *   forEach/map/filter/reduce/etc - O(n)
 */

/**When to use objects**
 *   When you need order.
 *   When you need fast access / insertion and removal depending on what part of the array
 */
